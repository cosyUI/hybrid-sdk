public class CloseWinData {
	/**
	 * 窗口名字
	 */
	private String name;
	/**
	 * 关闭窗口动画
	 */
	private String animation;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAnimation() {
		return animation;
	}

	public void setAnimation(String animation) {
		this.animation = animation;
	}
}
